/* Tipos de dados em JS

      Number: números inteiros ou decimais
      Strings: Caracteres envolvidos por aspas
      Boolean: true or false: verdadeiro ou falso
      Null: Seta, explicitando uma varável sem valor
      Undefined: variável sem identificação
      Object: Estruturas de dados complexas: Arrays, datas, Literais,...etc
      Symbol: Utilizado com objetos
      BigInt: Utilizado para manipular números muito grandes
*/